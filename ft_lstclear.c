#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void	*))
{
	int	i;

	i = 1;
	if (lst)
	{
		while (lst[i] != 0)
		{
			del((lst[i]));
			free(lst[i]);
			i++;
		}
	}
}
